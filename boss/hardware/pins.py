# Pin constants for B.O.S.S. hardware

# Button pins
BTN_RED_PIN = 26      # orange   + grey gnd
BTN_YELLOW_PIN = 19   # yellow
BTN_GREEN_PIN = 13    # green
BTN_BLUE_PIN = 6      # blue
MAIN_BTN_PIN = 17     # + gnd

# LED pins
LED_RED_PIN = 21
LED_YELLOW_PIN = 20
LED_GREEN_PIN = 16
LED_BLUE_PIN = 12

# Multiplexer pins
MUX1_PIN = 23  # C / 9    orange
MUX2_PIN = 24  # B / 10   yellow
MUX3_PIN = 25  # A / 11   green
MUX_IN_PIN = 8 #          blue

# TM1637 7-segment display
TM_CLK_PIN = 29 # GPIO 5 = BCM 29
TM_DIO_PIN = 7  # GPIO 4 = BCM 7
